# Python Comparison operators are used to compare two values:
#
# Operator	        Name	                        Example
# ==	            Equal	                        x == y
# !=	            Not equal	                    x != y
# >	                Greater than	                x > y
# <	                Less than	                    x < y
# >=	            Greater than or equal to	    x >= y
# <=	            Less than or equal to	        x <= y

x=5
y=10
# return a boolean value
print("Compare:", x<y)
print("Compare:", x>y)
print("Compare:", x>=y)
print("Compare:", x<=y)
print("Compare:",x!=y)
print("Compare:",x==y)
