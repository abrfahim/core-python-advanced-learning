# Looping means repeating something over and over until a particular condition is satisfied.

busket = ["banana", "mango", "tetulum", "jackfruit", "BlueBerry", "Naspati", "Apple", "Aamra"]

# for loop
# fruit is i or iterator variable
for fruit in busket:
    print("Fruit's Name:", fruit)

for i in range(1,11):
    print(i)

# while loop
i=0

while i<=5:
    print("whileloop:",i)
    i = i+1